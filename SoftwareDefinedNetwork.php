							<p class="mb-4 mb-md-5 ">Software-defined networking (SDN) is the disjoin of the network control logic from the devices performing the function, such as routers, which control the movement of information in the underlying network. This approach simplifies the management of infrastructure, which may be specific to one organization or break-up to be shared among several.</p>
